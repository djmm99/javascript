function parimpar(n) {  // 1. Definição da função com o parâmetro 'n'
    if(n%2 == 0) {
        return 'Par!';        // Retorno se for verdadeiro
    } else {
        return 'Impar!';        // Retorno se for falso                              5cm altura
    }
}

